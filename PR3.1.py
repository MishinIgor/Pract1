a = int(input('Введите целое число: '))
if a%2==0:
    if a > 0:
        print("Чётное положительное число")
    elif a == 0:
        print('Нулевое число')
    else:
        print("Чётное отрицательное число")
else:
    if a > 0:
        print("Нечётное положительное число")
    else:
        print("Нечётное отрицательное число")
